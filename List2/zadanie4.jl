  Proots=roots(P1)
  for k in 1:20
    zk = Proots[k]
    
    # println(k,"&",zk,"&",abs(polyval(P1, zk)),"&",abs(polyval(p, zk)),"\\","\\")
    println(k,"&",abs(zk-k),"\\","\\")
  end
  println("\n\n\n")
end

